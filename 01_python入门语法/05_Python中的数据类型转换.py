# 将数字类型转换为字符串类型
num_str = str(11)
print(type(num_str), num_str)
float_str = str(11.345)
print(type(float_str), float_str)
# 将字符串为数字类型
num = int("11")
print(type(num), num)

# 字符串转为浮点数
num2 = float("11.345")
print(type(num2), num2)

# 整数转换为浮点数
num_float = float(12)
print(type(num_float), num_float)

# 浮点数转整数(会丢失精度)
int_num = int(11.345)
print(type(int_num), int_num)
