"""
算数(数学)运算符: + - * / %(取余) //(取整除) **(指数[平方])
赋值运算符:
标准赋值=
复合赋值 += -= *= /= //= %= **=
"""
# 算数运算符
print("1+1=", 1 + 1)
print("2-1=", 2 - 1)
print("2*2=", 2 * 2)
print("4/2=", 4 / 2)
print("11//2=", 11 // 2)
print("9 % 2=", 9 % 2)
print("2**3=", 2 ** 3)

# 赋值运算符
num = 1
num += 1
print("num +=",num)
