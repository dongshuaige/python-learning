"""
字符串格式化数字精度控制 m.n
m: 控制宽度,要求是数字(很少使用), 设置的宽度小于数字自身,不生效
n: 控制小数点精度,要求是数字,会进行小数的四舍五入
eg:
  %5d 表示将整数的宽度控制在5位,如数字11,被设置为5d,就会变成[空格][空格][空格]11,用3个空格补足宽度
  %7.2f 11.345 [空格][空格]11.35
"""
num1 = 11
num2 = 11.345
print("数字11限制宽度5,结果是:%5d" % num1)
print("数字11.345限制宽度7,小数精度2,结果是:%7.2f" % num2)
print("数字11.345不限制宽度,小数精度2,结果是:%.2f" % num2)
print("数字11.345限制宽度2,小数精度2,结果是:%2.2f" % num2)
